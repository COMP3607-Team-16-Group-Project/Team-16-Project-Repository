//------------------
//Import Statements:
//------------------
import java.io.File;
//------------------




//--------------------------------------------------------------------------------------------------------------------------
//Class Body:
//--------------------------------------------------------------------------------------------------------------------------
public class NamingConvention implements Evaluation {
    //----------------------------------------------------------------------------------------------------------------------
    //Attributes:
    //----------------------------------------------------------------------------------------------------------------------
    private String feedback;
    //----------------------------------------------------------------------------------------------------------------------




    //----------------------------------------------------------------------------------------------------------------------
    //Methods:
    //----------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    //Evaluate Method:
    //-------------------------------------------------------------------------
    @Override
    public int evaluate(File javaFile) {
        //---------------------------------------------------------------------
        //Initialize Variables:
        //---------------------------------------------------------------------
        String fileName = javaFile.getName();
        //---------------------------------------------------------------------




        //---------------------------------------------------------------------
        //Check for proper naming:
        //---------------------------------------------------------------------
        if (Character.isUpperCase(fileName.charAt(0))) {
            feedback = "Naming convention passed.";
            return 10;
        } else {
            feedback = "File name should start with an uppercase letter.";
            return 5;
        }
        //---------------------------------------------------------------------
    }
    //-------------------------------------------------------------------------




    //-------------------------------------------------------------------------
    //Getter Methods:
    //-------------------------------------------------------------------------
    @Override
    public String getFeedback() {
        return feedback;
    }
    //-------------------------------------------------------------------------
}
